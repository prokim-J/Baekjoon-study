package Baekjoon1;

import java.util.Scanner;

public class stage9 {

	public static void main(String[] args) {
		// 사칙연산
		Scanner sc = new Scanner(System.in);
		int a, b;
		a = sc.nextInt();
		b = sc.nextInt();

		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);

	}

}
